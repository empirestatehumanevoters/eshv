          <strong>JOINING OTHER CITIES IN BANNING THE SALE OF FUR</strong>
          <p><strong>Problem:</strong>
It’s no secret that the fur industry is unthinkably cruel. Fur farms breed and confine animals in tiny, filthy cages where they suffer intense stress. They are beaten, bludgeoned, anally/vaginally electrocuted, and often skinned alive. And trapping animals in the wild is no better! Animals are trapped in cruel leg hold traps where they can suffer for days in excruciating pain — starving, until the trapper returns to shoot or bludgeon them to death. Mother coyotes are known to attempt to chew off their own legs trying to break free, desperate to get back to their young pups. Many other animals are unintentionally caught and killed in these traps, including household pets.</p>

<p><strong>Solution:</strong>
We support Intro 1476, sponsored by Speaker Corey Johnson, that would ban the sale of fur in NYC. We also support the proposed state ban on the sale of fur by Assembly Member Linda Rosenthal (A5040).  We take the bold stand against a cruel and dying industry and position ourselves on the right side of history. West Hollywood,  Berkeley, and San Francisco have enacted bans on the sale of fur and NYC will join them as progressive pioneers. Several countries — Norway, Croatia, Czech Republic, Germany, Japan, and the U.K. — have taken steps to close fur farms. With public awareness and innovative alternatives on the rise, popular designers such as Versace, Gucci, Michael Kors, BCBG Max Azria, Gap Inc., and The North Face have stopped using all animal fur. New York can get quite cold, but there is never an excuse to contribute to such suffering when so many viable, stylish, and warm ethical alternatives exist such as <a href="https://vautecouture.com/" target="_blank">Vaute Couture</a>, <a href="https://www.hoodlamb.com/" target="_blank">Hemp Hoodlamb</a>, <a href="https://www.wullyouterwear.com/" target="_blank">Wully Outerwear</a>, <a href="https://savetheduckusa.com/" target="_blank">Save The Duck</a>, <a href="https://noizeoriginal.ca/" target="_blank">Noize</a>, and more. Join us in this momentous undertaking.</p>
